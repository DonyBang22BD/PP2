b = 'Hello, World!'
print(b[2:5])
# Note: when you write b[2:5] the last number is not inclusive

#Get the characters from the start to position 5 (not included):
b = 'Hello, World!'
print(b[:5])

#starts from 2 goes till the end
b = "Hello, World!"
print(b[2:])
 

#From: "o" in "World!" (position -5)
#To, but not included: "d" in "World!" (position -2): 

b = "Hello, World!"
print(b[-5:-2])